# Simple Python program

# Ask for user input
name = input("Enter your name: ")
age = int(input("Enter your age: "))

# Print greeting
print(f"Hello, {name}!")

# Check if user is minor or adult
if age < 18:
    print("You are a minor.")
else:
    print("You are an adult.")

# End of program
print("Have a great day!")
